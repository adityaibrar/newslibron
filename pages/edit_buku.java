private void tampilin(String id) {
        try {
            Connection con = new database.connection().configDB();
            String sql = "SELECT * FROM buku WHERE id_buku = '"+id+"'";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            
            if (hasil.next()) {
                kd_buku.setText(hasil.getString("kode"));
                judul_buku.setText(hasil.getString("judul"));
                id_pengarang.setText(hasil.getString("pengarang"));
                tanggal_terbit.setText(hasil.getString("tanggal"));
                id_penerbit.setText(hasil.getString("penerbit"));
                no_rak.setText(hasil.getString("rak"));
                stock.setText(hasil.getString("stok"));
                jenis_buku.setSelectedItem(hasil.getString("jenis"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan Frame!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            this.setVisible(false);
        }
    }
