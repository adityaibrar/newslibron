    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        String getpengarang, gettanggal,getrak,getpenerbit, getkode,getstok, getjenis;
        getpengarang = id_pengarang.getText();
        gettanggal = tanggal_terbit.getText();
        getrak = no_rak.getText();
        getpenerbit =id_penerbit.getText();
        getkode = kd_buku.getText();
        getstok = stock.getText();
        getjenis = (String)jenis_buku.getSelectedItem();
        
         if (empty(gettanggal) || empty(getpengarang) || empty(getpenerbit) || empty(getstok) || empty(getrak)) {
            JOptionPane.showMessageDialog(null, "Data yang Anda masukkan belum lengkap!", "Peringatan!", JOptionPane.WARNING_MESSAGE);
        }else {
            try {
                Connection con = new database.connection().configDB();
                String sql = "UPDATE buku SET kode = '" + getkode + "', jenis =  '" + getjenis + "', tanggal = '" + gettanggal + "', pengarang = '" + getpengarang + "', rak = '" + getrak + "', penerbit = '" + getpenerbit + "', stok = '" + getstok + "' WHERE id_buku = '"+idnya.getText()+"'";
                java.sql.PreparedStatement stat = con.prepareStatement(sql);
                stat.executeUpdate();
                stat.close();
                con.close();
                new master.buku().tampil_buku();

                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Data Buku berhasil diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Buku tidak dapat diubah", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
         }
    }//GEN-LAST:event_simpanMouseClicked

