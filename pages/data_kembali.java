private void search_transaksi() {
        String query = search_transaksi.getText();
        
       Object []baris = {"ID", "NIS", "Nama", "Code", "Kode Buku", "Judul Buku", "Jenis Buku", "Tanggal Pinjam", "Tanggal Kembali", "Stok", "Denda"};
        tbl2 = new DefaultTableModel(null, baris) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tb_kembali.setModel(tbl2);
        
        try {
            Connection con = new database.connection().configDB();
            String sql ="SELECT * FROM kembali JOIN buku ON kembali.id_buku = buku.id_buku JOIN siswa ON siswa.id_siswa = kembali.id_siswa WHERE CONCAT(id_kembali, siswa.nis, siswa.nama, siswa.kode, siswa.jurusan, buku.kode, buku.judul, buku.jenis, kembali.tgl_pinjam, kembali.tgl_kembali) LIKE '%"+query+"%'";
            java.sql.Statement stmt = con.createStatement();
            java.sql.ResultSet hasil = stmt.executeQuery(sql);
            
            while (hasil.next()) {
                String idpinjam = hasil.getString("kembali.id_kembali");
                String nis = hasil.getString("siswa.nis");
                String nama = hasil.getString("siswa.nama");
                String code = hasil.getString("siswa.kode");
                String kode = hasil.getString("buku.kode");
                String judul = hasil.getString("buku.judul");
                String jenis = hasil.getString("buku.jenis");
                String tanggal_pnjm = hasil.getString("kembali.tgl_pinjam");
                String tanggal_kmbl = hasil.getString("kembali.tgl_kembali");
                String stok = hasil.getString("kembali.stok");
                String denda = hasil.getString("kembali.denda");
             
                String[] data = {idpinjam, nis, nama, code, kode, judul, jenis, tanggal_pnjm, tanggal_kmbl, stok, denda};
                tbl2.addRow(data);
                lebarKolom();
            }
            
            if (!query.equals("")) {
                result_siswa.setText("Hasil pencarian dari: '"+query+"'");
            } else {
                result_siswa.setText("");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

