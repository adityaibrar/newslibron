private void tampil_data() {
        Object []baris = {"ID", "NIS", "Nama", "Jurusan", "Kode Buku", "Judul Buku", "Jenis Buku", "Tanggal Pinjam", "Tanggal Kembali", "Stok", "Denda"};
        tbl2 = new DefaultTableModel(null, baris) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tb_kembali.setModel(tbl2);
        try {
            Connection con = new database.connection().configDB();
            String sql = "SELECT siswa.*, buku.*, kembali.* FROM siswa, buku, kembali WHERE siswa.id_siswa = kembali.id_siswa AND buku.id_buku = kembali.id_buku ORDER BY id_kembali ASC";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            
            while (hasil.next()) {
                String idpinjam = hasil.getString("kembali.id_kembali");
                String nis = hasil.getString("siswa.nis");
                String nama = hasil.getString("siswa.nama");
                String jurusan = hasil.getString("siswa.jurusan");
                String kode = hasil.getString("buku.kode");
                String judul = hasil.getString("buku.judul");
                String jenis = hasil.getString("buku.jenis");
                String tanggal_pnjm = hasil.getString("kembali.tgl_pinjam");
                String tanggal_kmbl = hasil.getString("kembali.tgl_kembali");
                String stok = hasil.getString("kembali.stok");
                String denda = hasil.getString("kembali.denda");
                
                if (hasil.getString("jenis").equals("1")) {
                    jenis = "Ensiklopedia";
                } else if (hasil.getString("jenis").equals("2")) {
                    jenis = "Novel";
                } else if (hasil.getString("jenis").equals("3")) {
                    jenis = "Komik";
                } else if (hasil.getString("jenis").equals("4")) {
                    jenis = "Biografi";
                } else if (hasil.getString("jenis").equals("5")) {
                    jenis = "Dongeng";
                } else if (hasil.getString("jenis").equals("6")) {
                    jenis = "Karya Ilmiah";
                }
                
                String[] data = {idpinjam, nis, nama, jurusan, kode, judul, jenis, tanggal_pnjm, tanggal_kmbl, stok, denda};
                tbl2.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menampilkan DATA!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
            dispose();
        }
    }

