    public void kode(){
         cmb_id = (String) jenis_buku.getSelectedItem();
    if ("Ensiklopedia".equals(cmb_id)) {
     id_buku= "ESP"+getRandom();
      } else if ("Novel".equals(cmb_id)) {
     id_buku = "NVL"+getRandom();
      } else if ("Komik".equals(cmb_id)) {
     id_buku = "KMK"+getRandom();
      } else if("Biografi".equals(cmb_id)){
       id_buku = "BGF"+getRandom();
      }else if("Dongeng".equals(cmb_id)){
          id_buku = "DGG"+getRandom();
      } else if("Karya Ilmiah".equals(cmb_id)){
          id_buku = "KIR"+getRandom();
      } 
    kd_buku.setText(id_buku);
    }

