    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked

        String getkode, getjenis, getjudul, gettanggal, getpengarang, getpenerbit, getstok, getrak;

        getkode = kd_buku.getText();
        getjenis = (String)jenis_buku.getSelectedItem();
        getjudul = judul_buku.getText();
        gettanggal = tanggal_terbit.getText();
        getpengarang = id_pengarang.getText();
        getpenerbit = id_penerbit.getText();
        getstok = stock.getText();
        getrak = no_rak.getText();

        if (empty(getjudul) || empty(gettanggal) || empty(getpengarang) || empty(getpenerbit) || empty(getstok) || empty(getrak)) {
            JOptionPane.showMessageDialog(null, "Data yang Anda masukkan belum lengkap!", "Peringatan!", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                Connection con = new database.connection().configDB();
                String get = "SELECT * FROM buku WHERE judul = '"+getjudul+"'";
                java.sql.Statement st = con.createStatement();
                java.sql.ResultSet hasil = st.executeQuery(get);

                if (hasil.next()) {
                    JOptionPane.showMessageDialog(null, "Judul buku tidak boleh ada yang sama.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                } else {
                    try {
                        String sql = "INSERT INTO buku VALUES (null, ?, ?, ?, ?, ?, ?, ?, ?)";
                        java.sql.PreparedStatement stat = (PreparedStatement)con.prepareStatement(sql);
                        stat.setString(1, getkode);
                        stat.setString(2, getjenis);
                        stat.setString(3, getjudul);
                        stat.setString(4, gettanggal);
                        stat.setString(5, getpengarang);
                        stat.setString(6, getpenerbit);
                        stat.setString(7, getstok);
                        stat.setString(8, getrak);
                        stat.executeUpdate();
                        
                        this.setVisible(false);
                        JOptionPane.showMessageDialog(null, "Data Buku Berhasil Ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Data Buku Gagal Disimpan", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (Exception e) {}
        }
    }//GEN-LAST:event_simpanMouseClicked

