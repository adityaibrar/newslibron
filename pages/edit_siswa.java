private void tampilin(String id) {
        try {
            Connection con = new database.connection().configDB();
            String sql = "SELECT * FROM siswa WHERE id_siswa = '"+id+"'";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            
            if (hasil.next()) {
                no_nis.setText(hasil.getString("nis"));
                nama_siswa.setText(hasil.getString("nama"));
                tpt_lhr.setText(hasil.getString("tempat_lahir"));
                tgl_lhr.setText(hasil.getString("tanggal_lahir"));
                jenis_jurusan.setSelectedItem(hasil.getString("jurusan"));
                tgl_msk.setText(hasil.getString("tgl_masuk"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan Frame!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            this.setVisible(false);
        }
    }
