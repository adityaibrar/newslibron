    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        String getnis = no_nis.getText();
        String getnama = nama_siswa.getText();
        String gettempat = tpt_lhr.getText();
        String gettl = tgl_lhr.getText();
        String getjurusan = (String)jenis_jurusan.getSelectedItem();
        
        if (empty(getnama) || empty(gettempat) || empty(gettl) || empty(getjurusan)) {
            JOptionPane.showMessageDialog(null, "Data yang Anda masukkan belum lengkap!", "Peringatan!", JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                Connection con = new database.connection().configDB();
                String sql = "UPDATE siswa SET nama = '" + getnama + "', tempat_lahir = '" + gettempat + "', tanggal_lahir = '" + gettl + "', jurusan = '" + getjurusan + "' WHERE id_siswa = '"+idnya.getText()+"'";
                java.sql.PreparedStatement stat = con.prepareStatement(sql);
                stat.executeUpdate();
                stat.close();
                con.close();

                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Data Siswa berhasil diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Siswa tidak dapat diubah", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_simpanMouseClicked

