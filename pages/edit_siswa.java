           public void kode(){
         cmb_id = (String) jenis_jurusan.getSelectedItem();
    if ("Teknik Komputer dan Jaringan".equals(cmb_id)) {
     id_siswa= "TKJ"+getRandom();
      } else if ("Teknik Kendaraan Ringan Otomotif".equals(cmb_id)) {
     id_siswa = "TKR"+getRandom();
      } else if ("Teknik dan Bisnis Sepeda Motor".equals(cmb_id)) {
     id_siswa = "TBSM"+getRandom();
      } else if("Otomatisasi dan Tata Kelola Perkantoran".equals(cmb_id)){
       id_siswa = "OTP"+getRandom();
      }else if("Akuntansi dan Keuangan Lembaga".equals(cmb_id)){
          id_siswa = "AKL"+getRandom();
      } else if("Bisnis Daring dan Pemasaran".equals(cmb_id)){
          id_siswa = "BDP"+getRandom();
      } 
    no_nis.setText(id_siswa);
    }
           
           private String getRandom() {
        for (int length = 0; length < 3; length++) {
            Character character = chars[random.nextInt(chars.length)];
            stringBuilder.append(character);
        }
        output = stringBuilder.toString();
        stringBuilder.delete(0, 6);
        return output;
    }
           
