    private void kembaliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kembaliMouseClicked
        // TODO add your handling code here:
        int baris = tb_transaksi.getSelectedRow();
        if (baris >= 0) {
            String idpinjam = tbl2.getValueAt(baris, 0).toString();
            System.out.println(idpinjam);
            
            try {
                Connection con = new database.connection().configDB();
                String sql = "SELECT siswa.*, buku.*, pinjam.* FROM siswa, buku, pinjam WHERE pinjam.id_pinjam = '"+idpinjam+"' ";
                java.sql.Statement stat = con.createStatement();
                java.sql.ResultSet rs = stat.executeQuery(sql);
                
                if (rs.next()) {
                    String id = rs.getString("id_pinjam");
                    System.out.println(id);
                    Frame frame = new Frame();
                    new pages.transaksi_kembali(frame, rootPaneCheckingEnabled, id).setVisible(true);
                }
            
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal menyeleksi data!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data yang akan diubah!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
        lebarKolom();
    }//GEN-LAST:event_kembaliMouseClicked

