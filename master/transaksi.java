     private void tampil_peminjaman() {
        Object []baris = {"ID", "NIS", "Nama", "jurusan", "Kode Buku", "Judul Buku", "Jenis Buku", "Tanggal Pinjam", "Tanggal Kembali"};
        tbl2 = new DefaultTableModel(null, baris) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tb_transaksi.setModel(tbl2);
        try {
            Connection con = new database.connection().configDB();
            String sql = "SELECT siswa.*, buku.*, pinjam.* FROM siswa, buku, pinjam WHERE siswa.id_siswa = pinjam.id_siswa AND buku.id_buku = pinjam.id_buku ORDER BY id_pinjam ASC";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            
            while (hasil.next()) {
                String idpinjam = hasil.getString("pinjam.id_pinjam");
                String nis = hasil.getString("siswa.nis");
                String nama = hasil.getString("siswa.nama");
                String code = hasil.getString("siswa.jurusan");
                String kode = hasil.getString("buku.kode");
                String judul = hasil.getString("buku.judul");
                String jenis = hasil.getString("buku.jenis");
                String tanggal_pnjm = hasil.getString("pinjam.tgl_pinjam");
                String tanggal_kmbl = hasil.getString("pinjam.tgl_kembali");
                
                String[] data = {idpinjam, nis, nama, code, kode, judul, jenis, tanggal_pnjm, tanggal_kmbl};
                tbl2.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menampilkan DATA!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
            dispose();
        }
    }
    

private void search_transaksi() {
        String query = search_transaksi.getText();
        
       Object []baris = {"ID", "NIS", "Nama", "Code", "Kode Buku", "Judul Buku", "Jenis Buku", "Tanggal Pinjam", "Tanggal Kembali"};
        tbl2 = new DefaultTableModel(null, baris) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tb_transaksi.setModel(tbl2);
        
        try {
            Connection con = new database.connection().configDB();
            String sql ="SELECT * FROM pinjam JOIN buku ON pinjam.id_buku = buku.id_buku JOIN siswa ON siswa.id_siswa = pinjam.id_siswa WHERE CONCAT(id_pinjam, siswa.nis, siswa.nama, siswa.kode, siswa.jurusan, buku.kode, buku.judul, buku.jenis, pinjam.tgl_pinjam, pinjam.tgl_kembali) LIKE '%"+query+"%'";
            java.sql.Statement stmt = con.createStatement();
            java.sql.ResultSet hasil = stmt.executeQuery(sql);
            
            while (hasil.next()) {
                String idpinjam = hasil.getString("pinjam.id_pinjam");
                String nis = hasil.getString("siswa.nis");
                String nama = hasil.getString("siswa.nama");
                String code = hasil.getString("siswa.kode");
                String kode = hasil.getString("buku.kode");
                String judul = hasil.getString("buku.judul");
                String jenis = hasil.getString("buku.jenis");
                String tanggal_pnjm = hasil.getString("pinjam.tgl_pinjam");
                String tanggal_kmbl = hasil.getString("pinjam.tgl_kembali");
             
                String[] data = {idpinjam, nis, nama, code, kode, judul, jenis, tanggal_pnjm, tanggal_kmbl};
                tbl2.addRow(data);
                lebarKolom();
            }
            
            if (!query.equals("")) {
                result_siswa.setText("Hasil pencarian dari: '"+query+"'");
            } else {
                result_siswa.setText("");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

